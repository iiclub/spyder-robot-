import { DEFAULT_PINS, LEG_IDS, SERVO_CONFIG } from './constants.js';

/**
 * Generate a complete Arduino sketch from recorded poses and methods.
 *
 * @param {Array} poses   - [{angles: {FL:{hip,shoulder,knee},...}, transitionMs: 500}, ...]
 * @param {Array} methods - [{name: string, poses: [...same format...]}, ...]
 * @returns {string} Complete Arduino .ino file contents
 */
export function generateArduinoCode(poses, methods) {
    const lines = [];

    // ── Header ─────────────────────────────────────────────────────────
    lines.push('// ═══════════════════════════════════════════════════════════');
    lines.push('// Spider Robot - Generated by Quadruped IK Simulator');
    lines.push('// 12-Servo Quadruped Controller');
    lines.push('// ═══════════════════════════════════════════════════════════');
    lines.push('');
    lines.push('#include <Servo.h>');
    lines.push('');

    // ── Pin definitions ────────────────────────────────────────────────
    lines.push('// ── Pin Definitions ──');
    for (const leg of LEG_IDS) {
        const pins = DEFAULT_PINS[leg];
        lines.push(`#define ${leg}_HIP_PIN      ${pins.hip}`);
        lines.push(`#define ${leg}_SHOULDER_PIN  ${pins.shoulder}`);
        lines.push(`#define ${leg}_KNEE_PIN      ${pins.knee}`);
    }
    lines.push('');

    // ── Servo objects ──────────────────────────────────────────────────
    lines.push('// ── Servo Objects ──');
    for (const leg of LEG_IDS) {
        const l = leg.toLowerCase();
        lines.push(`Servo ${l}Hip, ${l}Shoulder, ${l}Knee;`);
    }
    lines.push('');

    // ── Init angles (calibration position: left=0, right=180) ────────
    const initArr = [];
    for (const leg of LEG_IDS) {
        initArr.push(
            SERVO_CONFIG[leg].hip.init,
            SERVO_CONFIG[leg].shoulder.init,
            SERVO_CONFIG[leg].knee.init
        );
    }
    lines.push('// ── Init / Calibration angles ──');
    lines.push('// Left servos init at 0, right servos init at 180');
    lines.push(`int initAngles[12] = {${initArr.join(',')}};`);
    lines.push('');

    // ── Smooth-move variables ──────────────────────────────────────────
    lines.push('// ── Smooth Movement ──');
    lines.push('// Current angles for interpolation (start at init position)');
    lines.push(`int currentAngles[12] = {${initArr.join(',')}};`);
    lines.push('');
    lines.push('// Servo array for easy indexed access');
    lines.push('Servo* servos[12];');
    lines.push('');

    // ── Helper: smoothMove ─────────────────────────────────────────────
    lines.push('// Move all 12 servos smoothly from current position to target');
    lines.push('void smoothMove(int target[12], int durationMs) {');
    lines.push('    int startAngles[12];');
    lines.push('    for (int i = 0; i < 12; i++) startAngles[i] = currentAngles[i];');
    lines.push('');
    lines.push('    int steps = max(durationMs / 20, 1);  // ~50Hz update rate');
    lines.push('    for (int s = 1; s <= steps; s++) {');
    lines.push('        for (int i = 0; i < 12; i++) {');
    lines.push('            int pos = startAngles[i] + (target[i] - startAngles[i]) * s / steps;');
    lines.push('            servos[i]->write(pos);');
    lines.push('            currentAngles[i] = pos;');
    lines.push('        }');
    lines.push('        delay(durationMs / steps);');
    lines.push('    }');
    lines.push('}');
    lines.push('');

    // ── Helper: goToInit ─────────────────────────────────────────────
    lines.push('// Go to init/calibration position (left=0, right=180)');
    lines.push('void goToInit() {');
    lines.push('    smoothMove(initAngles, 1000);');
    lines.push('}');
    lines.push('');

    // ── Generate method functions ──────────────────────────────────────
    for (const method of methods) {
        lines.push(`// ── Method: ${method.name} ──`);
        lines.push(`void ${sanitizeName(method.name)}() {`);
        for (let i = 0; i < method.poses.length; i++) {
            const pose = method.poses[i];
            const arr = anglesToArray(pose.angles);
            lines.push(`    int step${i + 1}[12] = {${arr.join(',')}};`);
            lines.push(`    smoothMove(step${i + 1}, ${pose.transitionMs});`);
        }
        lines.push('}');
        lines.push('');
    }

    // ── Generate the main sequence ─────────────────────────────────────
    if (poses.length > 0) {
        lines.push('// ── Main Sequence (recorded poses) ──');
        lines.push('void runSequence() {');
        for (let i = 0; i < poses.length; i++) {
            const pose = poses[i];
            const arr = anglesToArray(pose.angles);
            lines.push(`    int pose${i + 1}[12] = {${arr.join(',')}};`);
            lines.push(`    smoothMove(pose${i + 1}, ${pose.transitionMs});`);
        }
        lines.push('}');
        lines.push('');
    }

    // ── setup() ────────────────────────────────────────────────────────
    lines.push('void setup() {');
    lines.push('    Serial.begin(9600);');
    lines.push('    Serial.println("Spider Robot starting...");');
    lines.push('');
    const servoNames = [];
    for (const leg of LEG_IDS) {
        const l = leg.toLowerCase();
        const pins = DEFAULT_PINS[leg];
        lines.push(`    ${l}Hip.attach(${leg}_HIP_PIN);`);
        lines.push(`    ${l}Shoulder.attach(${leg}_SHOULDER_PIN);`);
        lines.push(`    ${l}Knee.attach(${leg}_KNEE_PIN);`);
        servoNames.push(`&${l}Hip`, `&${l}Shoulder`, `&${l}Knee`);
    }
    lines.push('');
    lines.push('    // Fill servo pointer array (FL hip,sh,kn, FR hip,sh,kn, BL..., BR...)');
    lines.push(`    Servo* ptrs[12] = {${servoNames.join(', ')}};`);
    lines.push('    for (int i = 0; i < 12; i++) servos[i] = ptrs[i];');
    lines.push('');
    lines.push('    // Start at init/calibration position');
    lines.push('    goToInit();');
    lines.push('    delay(2000);  // Wait 2 seconds before starting');
    lines.push('}');
    lines.push('');

    // ── loop() ─────────────────────────────────────────────────────────
    lines.push('void loop() {');
    if (poses.length > 0) {
        lines.push('    runSequence();');
    }
    for (const method of methods) {
        lines.push(`    // ${method.name}();  // Uncomment to use`);
    }
    if (poses.length === 0 && methods.length === 0) {
        lines.push('    // No poses recorded yet — record some in the simulator!');
        lines.push('    delay(1000);');
    }
    lines.push('}');

    return lines.join('\n');
}

/** Convert nested angles object to flat array [FL_hip, FL_sh, FL_kn, FR_hip, ...] */
function anglesToArray(angles) {
    const arr = [];
    for (const leg of LEG_IDS) {
        arr.push(angles[leg].hip, angles[leg].shoulder, angles[leg].knee);
    }
    return arr;
}

/** Sanitize a method name to be a valid C identifier */
function sanitizeName(name) {
    return name
        .replace(/[^a-zA-Z0-9_]/g, '_')
        .replace(/^[0-9]/, '_$&')
        || 'unnamed';
}
